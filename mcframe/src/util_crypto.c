typedef struct { u8 buf[64]; u32 len; unsigned long long bits; u32 h[8]; } SHA256;

static void sha256_compress(SHA256 *c, const u8 *b)
{
    u32 w[64],a,x,t1,t2; int i;
    for(i=0;i<16;i++) w[i]=((u32)b[i*4]<<24)|((u32)b[i*4+1]<<16)|((u32)b[i*4+2]<<8)|b[i*4+3];
    for(i=16;i<64;i++) w[i]=SH_G1(w[i-2])+w[i-7]+SH_G0(w[i-15])+w[i-16];
    a=c->h[0];x=c->h[1];u32 cc=c->h[2],d=c->h[3],e=c->h[4],f=c->h[5],g=c->h[6],h=c->h[7];
    for(i=0;i<64;i++){
        t1=h+SH_S1(e)+SH_CH(e,f,g)+K[i]+w[i];
        t2=SH_S0(a)+SH_MAJ(a,x,cc);
        h=g;g=f;f=e;e=d+t1;d=cc;cc=x;x=a;a=t1+t2;
    }
    c->h[0]+=a;c->h[1]+=x;c->h[2]+=cc;c->h[3]+=d;
    c->h[4]+=e;c->h[5]+=f;c->h[6]+=g;c->h[7]+=h;
}

static void sha256_init(SHA256 *c)
{
    c->len=0;c->bits=0;
    c->h[0]=0x6a09e667;c->h[1]=0xbb67ae85;c->h[2]=0x3c6ef372;c->h[3]=0xa54ff53a;
    c->h[4]=0x510e527f;c->h[5]=0x9b05688c;c->h[6]=0x1f83d9ab;c->h[7]=0x5be0cd19;
}

static void sha256_feed(SHA256 *c, const u8 *data, size_t n)
{
    size_t i;
    for(i=0;i<n;i++){
        c->buf[c->len++]=data[i];
        if(c->len==64){sha256_compress(c,c->buf);c->bits+=512;c->len=0;}
    }
}

static void sha256_done(SHA256 *c, u8 out[32])
{
    int i; c->bits+=c->len*8;
    c->buf[c->len++]=0x80;
    if(c->len>56){while(c->len<64)c->buf[c->len++]=0;sha256_compress(c,c->buf);c->len=0;}
    while(c->len<56)c->buf[c->len++]=0;
    for(i=7;i>=0;i--){c->buf[56+(7-i)]=(u8)(c->bits>>(i*8));}
    sha256_compress(c,c->buf);
    for(i=0;i<8;i++){out[i*4]=(u8)(c->h[i]>>24);out[i*4+1]=(u8)(c->h[i]>>16);out[i*4+2]=(u8)(c->h[i]>>8);out[i*4+3]=(u8)c->h[i];}
}

static void hmac_sha256(const u8 *key, size_t klen,
                        const u8 *msg, size_t mlen,
                        u8 out[32])
{
    u8 k0[64],pad[64],tmp[32]; SHA256 ctx; size_t i;
    memset(k0,0,64);
    if(klen>64){SHA256 kc;sha256_init(&kc);sha256_feed(&kc,key,klen);sha256_done(&kc,k0);}
    else memcpy(k0,key,klen);
    for(i=0;i<64;i++) pad[i]=k0[i]^0x36;
    sha256_init(&ctx);sha256_feed(&ctx,pad,64);sha256_feed(&ctx,msg,mlen);sha256_done(&ctx,tmp);
    for(i=0;i<64;i++) pad[i]=k0[i]^0x5c;
    sha256_init(&ctx);sha256_feed(&ctx,pad,64);sha256_feed(&ctx,tmp,32);sha256_done(&ctx,out);
}

