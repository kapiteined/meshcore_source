int util_crypto_from_hex(uint8_t *out, int out_max, const char *hex) {
  return from_hex((u8*)out, out_max, hex);
}

void util_crypto_hmac_sha256(const uint8_t *key, size_t klen,
                             const uint8_t *msg, size_t mlen,
                             uint8_t out[32]) {
  hmac_sha256((const u8*)key, klen, (const u8*)msg, mlen, (u8*)out);
}

void util_crypto_aes128_ecb_decrypt(const uint8_t key16[16],
                                    const uint8_t *in, uint8_t *out,
                                    int blocks) {
  aes128_ecb_decrypt((const u8*)key16, (const u8*)in, (u8*)out, blocks);
}

uint16_t util_crypto_mac16_le(const uint8_t key32[32], const uint8_t *ciphertext, size_t ct_len) {
  uint8_t h[32];
  hmac_sha256((const u8*)key32, 32, (const u8*)ciphertext, ct_len, (u8*)h);
  return (uint16_t)h[0] | ((uint16_t)h[1] << 8);
}
